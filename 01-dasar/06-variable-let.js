// let adalah variable yang dapat di ubah atau ditimpa nilainya
let fullname;
let firstname;
let middlename;
let lastname;

firstname = "Afista";
middlename = "Regian";
lastname = "Nandi";
fullname = firstname+" "+middlename+" "+lastname;

document.writeln("firstname = "+firstname);
document.writeln("</br>");
document.writeln("middlename = "+middlename);
document.writeln("</br>");
document.writeln("lastname = "+lastname);
document.writeln("</br>");
document.writeln("fullname = "+fullname);

// contoh let yang dapat diubah atau ditimpa oleh nilai yang baru
let buah = "Apel"; // nilai pertama
buah = "Mangga"; // nilai kedua
document.writeln("</br>");
document.writeln("</br>");
document.writeln("Buah = "+buah); // yang ditampilkan adalah nilai kedua