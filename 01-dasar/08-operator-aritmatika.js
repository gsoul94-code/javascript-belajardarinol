// operator aritmatika yang dapat dijalankan di dalam JS
/**
    + tambah
    - kurang
    * kali
    / bagi
    ** exponensial
    % mod ata sisa bagi
**/

let resTambah = 1 + 2;
document.writeln("1 + 2 = " + resTambah);
document.writeln("</br>");

let resKurang = 5 - 1;
document.writeln("5 - 1 = " + resKurang);
document.writeln("</br>");

let resKali = 2 * 3;
document.writeln("2 x 3 = " + resKali);
document.writeln("</br>");

let resBagi = 10 / 2;
document.writeln("10 : 2 = " + resBagi);
document.writeln("</br>");

let resExpo = 10 ** 2;
document.writeln("10 ** 2 = " + resExpo);
document.writeln("</br>");

let resMod = 10 / 3;
document.writeln("10 mod 3 = " + resMod);
document.writeln("</br>");