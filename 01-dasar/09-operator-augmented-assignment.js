/**
            operator augmented assignment sama saja dengan operator aritmatika,
            hanya saja, dikhususkan untuk melakukan operasi dengan variable itu sendiri.
        **/

/**
            operator augmented assigment yang dapat dijalankan di dalam JS :
            + tambah
            - kurang
            * kali
            / bagi
            ** exponensial
            % mod ata sisa bagi
        **/
let result = 1;
document.writeln("result = " + result);
document.writeln("</br>");

result += 2;
document.writeln("result + 2 = " + result);
document.writeln("</br>");

result -= 1;
document.writeln("result - 1 = " + result);
document.writeln("</br>");

result *= 5;
document.writeln("result x 5 = " + result);
document.writeln("</br>");

result /= 2;
document.writeln("result / 2 = " + result);
document.writeln("</br>");

result **= 2;
document.writeln("result ** 2 = " + result);
document.writeln("</br>");

result %= 2;
document.writeln("result mod 2 = " + result);
document.writeln("</br>");